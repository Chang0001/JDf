package com.fanwe.p2p.model;

public class PageModel
{

	private int page = 0;
	private int page_total = 0;

	public int getPage()
	{
		return page;
	}

	public void setPage(int page)
	{
		this.page = page;
	}

	public int getPage_total()
	{
		return page_total;
	}

	public void setPage_total(int page_total)
	{
		this.page_total = page_total;
	}

}
